import { getOverlay } from '../db.js';

function appendOverlay(base: string, model: 'claude' | 'ollama'): string {
  let overlay: string | null = null;
  try {
    overlay = getOverlay(model);
  } catch (err) {
    console.warn('prompt overlay read failed:', err instanceof Error ? err.message : err);
    return base;
  }
  if (!overlay || !overlay.trim()) return base;
  return `${base}\n\n## Додаткові інструкції\n${overlay}`;
}

function formatNow(): string {
  const now = new Date();
  const date = now.toLocaleDateString('uk-UA', {
    weekday: 'long',
    year: 'numeric',
    month: 'long',
    day: 'numeric',
  });
  const time = now.toLocaleTimeString('uk-UA', {
    hour: '2-digit',
    minute: '2-digit',
  });
  return `${date}, ${time}`;
}

